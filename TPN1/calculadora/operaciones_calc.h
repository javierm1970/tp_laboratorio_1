/**
*
*\brief Realiza la operación Suma de dos parametros.
*\param Recibe el valor de dos Operandos (int).
*\return Devuelve el Resultado (int) de Sumar el valor del primer Operando
*        al valor del segundo Operando.
*/

int suma (int operando1, int operando2);

/**
*
*\brief Realiza la operación Resta de dos parametros.
*\param Recibe el valor de dos Operandos (int).
*\return Devuelve el Resultado (int) de Restarle el valor del segundo Operando
*        al valor del primero Operando.
*/

int resta (int operando1, int operando2);

/**
*
*\brief Realiza la operación Multiplicación de dos parametros.
*\param Recibe el valor de dos Operandos (int).
*\return Devuelve el Resultado o Producto (int) de Multiplicar el primer Operando
*        por el segundo Operando.
*/

int multiplicacion (int operando1, int operando2);

/**
*
*\brief Realiza la operación División de dos parametros
*\param Recibe el valor de dos Operandos (int)
*\return Devuelve el Resultado (float) de Dividir al valor del primer Operando
*        el valor del segundo operando.
*/

float division (int operando1, int operando2);


/**
*
*\brief Realiza la operación Factorial de un parametros
*\param Recibe el valor de un Operando (int)
*\return Devuelve el Resultado (int) que surge de multiplicar sucesivamente
*        una variable 'resultado' que inicia con el valor del operando recibido
*        por otra variable que se incrementa en 1 hasta llegar al valor del (Operando - 1)
*
*/

int factorial (int operando1);

